# LA#2 - Comparison Operators
# Get two integer inputs from user
value_1 = int(input("Enter value for x: "))
value_2 = int(input("Enter value for y: "))

# Result of all comparison operations
print("\nResult:")
print(f"{value_1} > {value_2} = {value_1 > value_2}")
print(f"{value_1} >= {value_2} = {value_1 >= value_2}")
print(f"{value_1} < {value_2} = {value_1 < value_2}")
print(f"{value_1} <= {value_2} = {value_1 <= value_2}")
print(f"{value_1} == {value_2} = {value_1 == value_2}")
print(f"{value_1} != {value_2} = {value_1 != value_2}")
